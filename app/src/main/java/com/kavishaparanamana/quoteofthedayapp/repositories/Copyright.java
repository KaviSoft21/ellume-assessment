package com.kavishaparanamana.quoteofthedayapp.repositories;

public class Copyright {
    private String year;
    private String url;


    // Getter Methods

    public String getYear() {
        return year;
    }

    public String getUrl() {
        return url;
    }

    // Setter Methods

    public void setYear(String year) {
        this.year = year;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
